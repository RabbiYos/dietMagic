export const getFastingStageByHours = (hours) => {
  const h = Number(hours) || 0;
  let current = FASTING_STAGES[0];

  for (const stage of FASTING_STAGES) {
    if (h >= stage.minHours) current = stage;
    else break;
  }

  const next = FASTING_STAGES.find(s => s.minHours > current.minHours) || null;
  return { current, next };
};
